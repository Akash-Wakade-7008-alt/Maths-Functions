/**
 * <p>Calculates mean of two numbers</p>
 */
class Mean {

    double num1;
    double num2;

    /**
     * @param num1 first number
     * @param num2 second number
     * @return average of num1 and num2
     */
    double meanOfTwoNum(double num1, double num2) {
        return (num1 + num2) / 2.0;
    }
}